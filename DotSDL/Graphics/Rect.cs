namespace DotSDL.Graphics {
    /// <summary>
    /// Defines a rectangle.
    /// </summary>
    public struct Rect {
        /// <summary>
        /// The upper-left corner of the <see cref="Rect"/>.
        /// </summary>
        public Point Origin;

        /// <summary>
        /// The width of the <see cref="Rect"/>.
        /// </summary>
        public int Width;

        /// <summary>
        /// The height of the <see cref="Rect"/>.
        /// </summary>
        public int Height;
    }
}
