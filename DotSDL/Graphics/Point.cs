namespace DotSDL.Graphics {
    /// <summary>
    /// Represents a point in 2D space.
    /// </summary>
    public class Point {
        /// <summary>
        /// The X coordinate of the point.
        /// </summary>
        public int X { get; set; }

        /// <summary>
        /// The Y coordinate of the point.
        /// </summary>
        public int Y { get; set; }
    }
}
